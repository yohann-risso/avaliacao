UPDATE employees
SET termination_date = substring(deactivated_at from 1 for 10)
WHERE active = 0
  AND COALESCE(termination_date, '') = ''
  AND COALESCE(deactivated_at, '') <> '';

UPDATE employees
SET is_monitor = 0,
    monitor_start_date = ''
WHERE COALESCE(is_leadership, 0) = 1
  AND COALESCE(is_monitor, 0) = 1;

UPDATE employees
SET active = 0,
    deactivated_at = CASE
        WHEN COALESCE(deactivated_at, '') = '' THEN now()::timestamp(0)::text
        ELSE deactivated_at
    END
WHERE COALESCE(termination_date, '') <> ''
  AND termination_date <= current_date::text
  AND active <> 0;

UPDATE employees
SET active = 1,
    deactivated_at = ''
WHERE COALESCE(termination_date, '') <> ''
  AND termination_date > current_date::text
  AND active <> 1;

