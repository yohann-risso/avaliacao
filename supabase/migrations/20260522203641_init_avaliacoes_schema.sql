CREATE TABLE IF NOT EXISTS login_users (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    username TEXT NOT NULL,
    password_hash TEXT NOT NULL,
    role TEXT NOT NULL DEFAULT 'admin',
    evaluator_employee_id INTEGER,
    active INTEGER NOT NULL DEFAULT 1,
    last_login_at TEXT NOT NULL DEFAULT '',
    created_at TEXT NOT NULL,
    updated_at TEXT NOT NULL DEFAULT ''
);

CREATE UNIQUE INDEX IF NOT EXISTS idx_login_users_username_lower ON login_users (LOWER(username));
CREATE INDEX IF NOT EXISTS idx_login_users_active ON login_users(active, username);

